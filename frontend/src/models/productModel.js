
class Product {
    constructor(id, image, name, description, price, author, type, categories) {
      this.id = id;
      this.image = image;
      this.name = name;
      this.description = description;
      this.price = price;
      this.author = author;
      this.type = type;
      this.categories = categories;
    }
  }
  
  export default Product;
  